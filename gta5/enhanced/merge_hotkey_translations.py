def translation_key_for_vk(vk: int) -> str:
    vk &= 0xFF
    if 0x30 <= vk <= 0x39:
        return "KEY_" + chr(vk)
    if 0x41 <= vk <= 0x5A:
        return "KEY_" + chr(vk)
    if 0x70 <= vk <= 0x87:
        return f"KEY_F{vk - 0x6F}"
    if 0x60 <= vk <= 0x69:
        return f"KEY_NUMPAD_{vk - 0x60}"
    return SWITCH.get(vk, f"KEY_VK_{vk:02X}")


